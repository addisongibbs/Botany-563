library(ape)
library(adegenet)
library(phangorn)
dna <- fasta2DNAbin(file="SEQUENCE.fasta")
print("fasta")
dna2 <- as.phyDat(dna)
print("dna")
tre.ini <- njs(dist.dna(dna,model="raw"))
parsimony(tre.ini, dna2)
tre.pars <- optim.parsimony(tre.ini, dna2)
plot(tre.pars, cex=0.6)
title("plotted tree")
print("tre.pars")
